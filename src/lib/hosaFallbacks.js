// Generates fallback content dynamically if specific event key is not present
export function generateOfflineHosaResource(eventKey, type) {
  const normalizedKey = eventKey || 'medical-terminology';
  const resource = FALLBACK_RESOURCES[normalizedKey] || FALLBACK_RESOURCES['medical-terminology'];
  
  if (type === 'guide') {
    return resource.guide || `No study guide available for ${eventKey}. Please try again later.`;
  } else if (type === 'questions') {
    return resource.questions || `No questions available for ${eventKey}. Please try again later.`;
  } else if (type === 'mindmap') {
    return resource.mindmap || `No concept map available for ${eventKey}. Please try again later.`;
  }
  
  return `No offline resource available for this type.`;
}

